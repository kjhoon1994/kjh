package com.test025;

public class Sample {
	
	//초기화
	//객체(instance) 생성시
	//멤버변수(프로퍼티)의 값을 특정 값으로 할당하는 상태
	//멤버변수는 자동 초기화 지원 대상
	//정수형 멤버는 0, 실수형 멤버는 0.0
	//참조형 멤버는 null
	
	//기본 생성자 자동 생성
	
	//자동 초기화 지원 대상
	private int a;
	private double b;
	private String c;
	
	public int getA() {
		return this.a;
	}
	public double getB() {
		return this.b;
	}
	public String getC() {
		return this.c;
	}
	
	
}
